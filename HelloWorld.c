#include <stdio.h>

int main()
{
    // basic print statements
    printf("hello from C\n");
    printf("C is cool!\n");

    // variables
    int x = 123;
    float y = 1.23;
    char z = 'a';
    char a[] = "hello world";

    printf("%i\n", x);
    printf("%f\n", y);
    printf("%c\n", z);
    printf("%s", a);

    return 0;
}